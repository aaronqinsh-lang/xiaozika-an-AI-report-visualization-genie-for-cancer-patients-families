
import React from 'react';
import { createRoot } from 'react-dom/client';
import App from './app.tsx';

const container = document.getElementById('root');
if (container) {
  const root = createRoot(container);
  root.render(
    <React.StrictMode>
      <App />
    </React.StrictMode>
  );
} else {
  console.error("未能找到 id 为 'root' 的挂载点");
}
